// ─── Pinterest import ──────────────────────────────────────────────────────────

function PinterestSection() {
  const [connected,     setConnected]     = useState<boolean | null>(null); // null = still checking
  const [boards,        setBoards]        = useState<PinterestBoard[] | null>(null);
  const [selectedBoard, setSelectedBoard] = useState<PinterestBoard | null>(null);
  const [pins,          setPins]          = useState<PinterestPin[] | null>(null);
  const [loading,       setLoading]       = useState(false);
  const [loadError,     setLoadError]     = useState(false);

  useEffect(() => {
    // Clean up the OAuth redirect param so a page refresh doesn't re-show it.
    const params = new URLSearchParams(window.location.search);
    if (params.has("pinterest")) {
      window.history.replaceState({}, "", window.location.pathname);
    }
    fetch("/api/pinterest/status")
      .then(r => r.json())
      .then(d => setConnected(!!d.connected))
      .catch(() => setConnected(false));
  }, []);

  useEffect(() => {
    if (connected !== true) return;
    setLoading(true);
    setLoadError(false);
    fetch("/api/pinterest/boards")
      .then(r => r.json())
      .then(d => { if (d.boards) setBoards(d.boards); else setLoadError(true); })
      .catch(() => setLoadError(true))
      .finally(() => setLoading(false));
  }, [connected]);

  function openBoard(board: PinterestBoard) {
    setSelectedBoard(board);
    setPins(null);
    setLoading(true);
    setLoadError(false);
    fetch(`/api/pinterest/boards/${board.id}/pins`)
      .then(r => r.json())
      .then(d => { if (d.pins) setPins(d.pins); else setLoadError(true); })
      .catch(() => setLoadError(true))
      .finally(() => setLoading(false));
  }

  function disconnect() {
    fetch("/api/pinterest/disconnect", { method: "POST" }).then(() => {
      setConnected(false);
      setBoards(null);
      setSelectedBoard(null);
      setPins(null);
    });
  }

  if (connected === null) return null; // avoid a flash of the connect prompt while checking

  return (
    <div style={{ padding: "16px 16px 0" }}>
      <div style={{ display: "flex", alignItems: "center", justifyContent: "space-between", marginBottom: "10px" }}>
        <p style={{ fontSize: "13px", fontWeight: 600, color: "#000", letterSpacing: "-0.2px" }}>From Pinterest</p>
        {connected && (
          <button onClick={disconnect} style={{ fontSize: "11px", color: "#bbb", background: "none", border: "none", cursor: "pointer", textDecoration: "underline", textUnderlineOffset: "2px", fontFamily: "inherit" }}>
            Disconnect
          </button>
        )}
      </div>

      {!connected ? (
        <a href="/api/pinterest/connect" style={{
          display: "flex", alignItems: "center", justifyContent: "center", gap: "8px",
          padding: "16px", borderRadius: "14px", border: "1.5px dashed #e0dbd4",
          background: "#faf9f7", color: "#555", fontSize: "13px", fontWeight: 500,
          textDecoration: "none", marginBottom: "8px",
        }}>
          Connect Pinterest to import your boards
        </a>
      ) : selectedBoard ? (
        <div style={{ marginBottom: "8px" }}>
          <button onClick={() => { setSelectedBoard(null); setPins(null); }} style={{ fontSize: "12px", color: "#c9a96e", background: "none", border: "none", cursor: "pointer", marginBottom: "10px", padding: 0, fontFamily: "inherit" }}>
            ← All boards
          </button>
          <p style={{ fontSize: "13px", fontWeight: 600, color: "#000", marginBottom: "8px" }}>{selectedBoard.name}</p>
          {loading ? (
            <p style={{ fontSize: "12px", color: "#bbb" }}>Loading pins…</p>
          ) : loadError ? (
            <p style={{ fontSize: "12px", color: "#bbb" }}>Couldn&apos;t load pins.</p>
          ) : (pins ?? []).length === 0 ? (
            <p style={{ fontSize: "12px", color: "#bbb" }}>No pins in this board.</p>
          ) : (
            <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr 1fr", gap: "6px" }}>
              {(pins ?? []).map(pin => (
                <a key={pin.id} href={pin.link ?? "https://www.pinterest.com/"} target="_blank" rel="noopener noreferrer"
                  style={{ aspectRatio: "1", borderRadius: "10px", overflow: "hidden", background: "#f0ede8", display: "block" }}>
                  <ItemThumbnail src={pin.image ?? undefined} />
                </a>
              ))}
            </div>
          )}
        </div>
      ) : loading ? (
        <p style={{ fontSize: "12px", color: "#bbb", marginBottom: "8px" }}>Loading boards…</p>
      ) : loadError ? (
        <p style={{ fontSize: "12px", color: "#bbb", marginBottom: "8px" }}>Couldn&apos;t load boards.</p>
      ) : (boards ?? []).length === 0 ? (
        <p style={{ fontSize: "12px", color: "#bbb", marginBottom: "8px" }}>No boards found on your Pinterest account.</p>
      ) : (
        <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr", gap: "8px", marginBottom: "8px" }}>
          {(boards ?? []).map(board => (
            <button key={board.id} onClick={() => openBoard(board)} style={{ borderRadius: "16px", background: "#f7f6f3", overflow: "hidden", textAlign: "left", border: "none", cursor: "pointer", padding: 0 }}>
              <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr", gap: "2px", aspectRatio: "1", overflow: "hidden" }}>
                {[0, 1, 2, 3].map(i => (
                  <div key={i} style={{ background: PLACEHOLDER_COLORS[i], overflow: "hidden" }}>
                    {board.thumbnails[i] && <ItemThumbnail src={board.thumbnails[i]} />}
                  </div>
                ))}
              </div>
              <div style={{ padding: "10px 12px 8px" }}>
                <p style={{ fontSize: "13px", fontWeight: 600, color: "#000", letterSpacing: "-0.1px", overflow: "hidden", whiteSpace: "nowrap", textOverflow: "ellipsis" }}>{board.name}</p>
                <p style={{ fontSize: "11px", color: "#aaa" }}>{board.pinCount} {board.pinCount === 1 ? "pin" : "pins"}</p>
              </div>
            </button>
          ))}
        </div>
      )}
    </div>
  );
}

// ─── Comments ──────────────────────────────────────────────────────────────────

type CommunityComment = { id: string; lookId: string; author: string; text: string; createdAt: number };

function loadComments(lookId: string): CommunityComment[] {
  try {
    const all: CommunityComment[] = JSON.parse(localStorage.getItem("nomi_community_comments") ?? "[]");
    return all.filter(c => c.lookId === lookId).sort((a, b) => a.createdAt - b.createdAt);
  } catch { return []; }
}

function saveComment(comment: CommunityComment) {
  try {
    const all: CommunityComment[] = JSON.parse(localStorage.getItem("nomi_community_comments") ?? "[]");
    localStorage.setItem("nomi_community_comments", JSON.stringify([...all, comment]));
  } catch { /* quota exceeded */ }
}

function CommentsSection({ lookId }: { lookId: string }) {
  const [comments, setComments] = useState<CommunityComment[]>([]);
  const [text, setText] = useState("");

  useEffect(() => {
    setComments(loadComments(lookId));
  }, [lookId]);

  function handlePost() {
    const trimmed = text.trim();
    if (!trimmed) return;
    const author = (() => {
      try { return localStorage.getItem("nomi_user_name")?.trim() || "Anonymous"; } catch { return "Anonymous"; }
    })();
    const comment: CommunityComment = {
      id: Math.random().toString(36).slice(2, 10),
      lookId, author, text: trimmed, createdAt: Date.now(),
    };
    saveComment(comment);
    setComments(prev => [...prev, comment]);
    setText("");
  }

  return (
    <div style={{ padding: "20px 20px 0" }}>
      <p style={{ fontSize: "11px", fontWeight: 600, color: "#bbb", letterSpacing: "0.7px", textTransform: "uppercase", marginBottom: "12px" }}>
        Comments{comments.length > 0 ? ` (${comments.length})` : ""}
      </p>

      {comments.length === 0 ? (
        <p style={{ fontSize: "13px", color: "#bbb", marginBottom: "16px" }}>No comments yet — be the first.</p>
      ) : (
        <div style={{ display: "flex", flexDirection: "column", gap: "14px", marginBottom: "16px" }}>
          {comments.map(c => (
            <div key={c.id}>
              <div style={{ display: "flex", alignItems: "baseline", gap: "8px" }}>
                <span style={{ fontSize: "13px", fontWeight: 600, color: "#000" }}>{c.author}</span>
                <span style={{ fontSize: "11px", color: "#bbb" }}>{timeAgo(c.createdAt)}</span>
              </div>
              <p style={{ fontSize: "13px", color: "#444", lineHeight: 1.5, marginTop: "2px" }}>{c.text}</p>
            </div>
          ))}
        </div>
      )}

      <div style={{ display: "flex", gap: "8px", paddingBottom: "24px" }}>
        <input
          value={text}
          onChange={e => setText(e.target.value)}
          onKeyDown={e => { if (e.key === "Enter") handlePost(); }}
          placeholder="Add a comment..."
          style={{ flex: 1, padding: "10px 14px", borderRadius: "99px", border: "1.5px solid #e8e8e8", fontSize: "13px", outline: "none", fontFamily: "inherit" }}
        />
        <button
          onClick={handlePost}
          disabled={!text.trim()}
          style={{
            padding: "10px 16px", borderRadius: "99px", border: "none",
            background: text.trim() ? "#000" : "#f0ede8",
            color: text.trim() ? "#fff" : "#bbb",
            fontSize: "13px", fontWeight: 600, cursor: text.trim() ? "pointer" : "default",
            fontFamily: "inherit", flexShrink: 0,
          }}
        >
          Post
        </button>
      </div>
    </div>
  );
}

// ─── Community Detail ─────────────────────────────────────────────────────────

function CommunityDetail({ look, isSaved, onSave, onClose, onTagTap }: {
  look: CommunityLook; isSaved: boolean;
  onSave: (e: React.MouseEvent) => void; onClose: () => void;
  onTagTap?: (tag: string) => void;
}) {
  // Prefer full images array; fall back to legacy single image
  const allImages = look.images?.length ? look.images : (look.image ? [look.image] : []);
  const isMulti   = allImages.length > 1;

  function shopPiece(piece: CommunityPiece) {
    if (piece.productLink) { window.open(piece.productLink, "_blank", "noopener"); return; }
    if (piece.searchUrl)   { window.open(piece.searchUrl, "_blank", "noopener"); return; }
    const q = encodeURIComponent([piece.name, piece.store].filter(Boolean).join(" "));
    window.open(`https://www.google.com/search?tbm=shop&q=${q}`, "_blank", "noopener");
  }

  return (
    <div style={{ position: "fixed", inset: 0, background: "#fff", zIndex: 60, display: "flex", flexDirection: "column", alignItems: "center", overflowY: "auto" }}>
      <div style={{ width: "100%", maxWidth: "420px", paddingBottom: "80px" }}>

        {/* Hero — single image or horizontal scroll strip */}
        <div style={{ position: "relative" }}>
          {isMulti ? (
            /* Horizontal image strip for multi-image looks */
            <div style={{
              display: "flex", gap: "6px", overflowX: "auto",
              padding: "0 0 0 0", scrollbarWidth: "none",
              scrollSnapType: "x mandatory",
            } as React.CSSProperties}>
              {allImages.map((img, i) => (
                <div key={i} style={{
                  flexShrink: 0, width: "78vw", maxWidth: "320px", height: "340px",
                  background: "#f0ede8", overflow: "hidden",
                  scrollSnapAlign: "start",
                  borderRadius: i === 0 ? "0 0 0 0" : "0",
                }}>
                  {/* eslint-disable-next-line @next/next/no-img-element */}
                  <img src={img} alt="" style={{ width: "100%", height: "100%", objectFit: "cover", objectPosition: "top center", display: "block" }} />
                </div>
              ))}
              {/* Padding sentinel so last image can snap flush */}
              <div style={{ flexShrink: 0, width: "22vw", maxWidth: "100px" }} />
            </div>
          ) : (
            /* Single image — original hero layout */
            <div style={{ width: "100%", height: "380px", background: allImages[0] ? "#f0ede8" : "linear-gradient(160deg, #ede9e4 0%, #e2ddd7 100%)", overflow: "hidden" }}>
              {allImages[0] && (
                // eslint-disable-next-line @next/next/no-img-element
                <img src={allImages[0]} alt="" style={{ width: "100%", height: "100%", objectFit: "cover", objectPosition: "top center", display: "block" }} />
              )}
              <div style={{ position: "absolute", inset: 0, background: "linear-gradient(to top, rgba(0,0,0,0.5) 0%, rgba(0,0,0,0.0) 50%)" }} />
            </div>
          )}

          {/* Floating controls — always on top */}
          <div style={{ position: "absolute", top: 0, left: 0, right: 0, display: "flex", justifyContent: "space-between", padding: "20px", pointerEvents: "none" }}>
            <button onClick={onClose} style={{ ...floatBtn, pointerEvents: "auto" }}><BackArrow /></button>
            <button onClick={onSave} style={{ ...floatBtn, pointerEvents: "auto" }}>
              {isSaved ? <BookmarkFillSm /> : <BookmarkOutlineSm />}
            </button>
          </div>

          {/* Dot indicators for multi-image */}
          {isMulti && (
            <div style={{ display: "flex", justifyContent: "center", gap: "5px", padding: "8px 0 0" }}>
              {allImages.map((_, i) => (
                <div key={i} style={{ width: "5px", height: "5px", borderRadius: "50%", background: i === 0 ? "#000" : "#ddd" }} />
              ))}
            </div>
          )}
        </div>

        {/* Poster info */}
        <div style={{ padding: "18px 20px 0" }}>
          <p style={{ fontSize: "15px", fontWeight: 600, color: "#000", letterSpacing: "-0.2px" }}>{look.posterName}</p>
          <p style={{ fontSize: "12px", color: "#aaa", marginTop: "3px" }}>{timeAgo(look.sharedAt)}</p>
          {look.caption && (
            <p style={{ fontSize: "14px", color: "#555", lineHeight: 1.65, marginTop: "10px", fontStyle: "italic" }}>
              &ldquo;{look.caption}&rdquo;
            </p>
          )}
          {/* Hashtag pills */}
          {look.tags.filter(t => t.startsWith("#")).length > 0 && (
            <div style={{ display: "flex", flexWrap: "wrap", gap: "6px", marginTop: "12px" }}>
              {look.tags.filter(t => t.startsWith("#")).map(tag => (
                <button
                  key={tag}
                  onClick={() => onTagTap?.(tag)}
                  style={{
                    padding: "4px 11px", borderRadius: "99px",
                    border: "1px solid #e0dbd4", background: "none",
                    fontSize: "12px", color: "#aaa", cursor: onTagTap ? "pointer" : "default",
                    fontFamily: "inherit", transition: "color 0.15s, border-color 0.15s",
                  }}
                  onMouseEnter={e => { (e.currentTarget as HTMLButtonElement).style.color = "#c9a96e"; (e.currentTarget as HTMLButtonElement).style.borderColor = "#c9a96e"; }}
                  onMouseLeave={e => { (e.currentTarget as HTMLButtonElement).style.color = "#aaa"; (e.currentTarget as HTMLButtonElement).style.borderColor = "#e0dbd4"; }}
                >
                  {tag}
                </button>
              ))}
            </div>
          )}
        </div>

        {/* Shop this look — compact rows */}
        {look.pieces.length > 0 && (
          <div style={{ padding: "16px 20px 0" }}>
            <p style={{ fontSize: "11px", fontWeight: 600, color: "#bbb", letterSpacing: "0.7px", textTransform: "uppercase", marginBottom: "4px" }}>
              Shop this look
            </p>
            {look.pieces.map((piece, i) => (
              <button
                key={i}
                onClick={() => shopPiece(piece)}
                style={{
                  width: "100%", display: "flex", alignItems: "center", gap: "10px",
                  padding: "11px 0", background: "none", border: "none",
                  borderBottom: i < look.pieces.length - 1 ? "1px solid #f5f5f5" : "none",
                  cursor: "pointer", textAlign: "left",
                }}
              >
                <div style={{ flex: 1, minWidth: 0 }}>
                  <p style={{ fontSize: "13px", fontWeight: 600, color: "#000", letterSpacing: "-0.1px", overflow: "hidden", whiteSpace: "nowrap", textOverflow: "ellipsis" }}>{piece.name}</p>
                  {piece.store && <p style={{ fontSize: "11px", color: "#aaa", marginTop: "1px" }}>{piece.store}</p>}
                </div>
                {piece.price && <span style={{ fontSize: "12px", fontWeight: 600, color: "#c9a96e", flexShrink: 0 }}>{piece.price}</span>}
                <span style={{ fontSize: "12px", color: "#c9a96e", flexShrink: 0 }}>Shop →</span>
              </button>
            ))}
          </div>
        )}

        <div style={{ height: "1px", background: "#f0ede8", margin: "16px 20px 0" }} />
        <CommentsSection lookId={look.id} />
      </div>
    </div>
  );
}

