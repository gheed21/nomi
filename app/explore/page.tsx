// ─── Trends (static, generated by scripts/generate-trends.mjs) ───────────────

type TrendSource = { name: string; url: string | null };
type TrendItem   = { title: string; summary: string; sources: TrendSource[] };
type TrendsFile  = { generatedAt: string | null; trends: TrendItem[] };

const { generatedAt: trendsGeneratedAt, trends } = trendsRaw as TrendsFile;

function formatTrendDate(iso: string): string {
  return new Date(iso).toLocaleDateString("en-US", { month: "short", day: "numeric" });
}

// ─── Constants ────────────────────────────────────────────────────────────────

const FILTER_TABS = ["All", "OOTD", "Going out", "Events", "Weddings", "Work", "Secondhand"] as const;
type FilterTab = typeof FILTER_TABS[number];

function tabLabel(tab: FilterTab): string {
  if (tab === "All") return "All";
  const camel = tab.replace(/\s+(.)/g, (_, c) => c.toUpperCase());
  return `#${camel}`;
}

const _g = (label: string) =>
  OCCASION_GROUPS.find(g => g.label === label)!.options.map(normalizeTag);

const OCCASION_FILTER: Record<string, string[]> = {
  "OOTD":       _g("Everyday"),
  "Going out":  _g("Going out"),
  "Events":     _g("Events"),
  "Weddings":   _g("Weddings"),
  "Work":       _g("Work"),
  "Secondhand": _g("Secondhand"),
};

const OCCASION_SLUG_SET = new Set(
  OCCASION_GROUPS.flatMap(g => g.options.map(normalizeTag))
);

function slugToFilterTab(slug: string): FilterTab | null {
  const norm = normalizeTag(slug);
  for (const [tab, slugs] of Object.entries(OCCASION_FILTER)) {
    if (slugs.includes(norm)) return tab as FilterTab;
  }
  return null;
}

const PLACEHOLDER_COLORS = ["#f0ede8", "#ece9e4", "#e8e4dd", "#ede9e3"];

// ─── Helpers ─────────────────────────────────────────────────────────────────

function timeAgo(ts: number): string {
  const d = Date.now() - ts;
  const m = Math.floor(d / 60000);
  if (m < 2)  return "just now";
  if (m < 60) return `${m}m ago`;
  const h = Math.floor(m / 60);
  if (h < 24) return `${h}h ago`;
  const days = Math.floor(h / 24);
  if (days < 7) return `${days}d ago`;
  return new Date(ts).toLocaleDateString([], { month: "short", day: "numeric" });
}

// ─── Page ─────────────────────────────────────────────────────────────────────

export default function ExplorePage() {
  const router = useRouter();
  const [looks,     setLooks]     = useState<CommunityLook[]>([]);
  const [savedIds,  setSavedIds]  = useState<Set<string>>(new Set());
  const [activeTab, setActiveTab] = useState<FilterTab>("All");
  const [selected,  setSelected]  = useState<CommunityLook | null>(null);
  const [query,     setQuery]     = useState("");
  const feedRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const communityLooks: CommunityLook[] = JSON.parse(localStorage.getItem("nomi_community_looks") ?? "[]");

    // One-time migration: old community looks were saved before `look.images` was added to the
    // schema and only have the single `look.image` field. Find each one's corresponding saved
    // look by matching piece names and pull the real per-item image URLs from there.
    type SavedItem = { name?: string; image?: string; isOriginal?: boolean };
    type SavedLookRef = { items: SavedItem[] };
    const savedLooks: SavedLookRef[] = JSON.parse(localStorage.getItem("nomi_saved_looks") ?? "[]");

    let anyPatched = false;
    const patched = communityLooks.map(look => {
      if (look.images?.length) return look; // already has images — nothing to do

      // Find a saved look whose non-original items match this community look's pieces exactly
      const matched = savedLooks.find(sl => {
        const saveMatchNames = sl.items.filter(i => !i.isOriginal).map(i => i.name ?? "");
        return look.pieces.length > 0 &&
               look.pieces.length <= saveMatchNames.length &&
               look.pieces.every(p => saveMatchNames.includes(p.name));
      });
      if (!matched) return look;

      const originalImg = matched.items.find(i => i.isOriginal)?.image;
      const matchImgs   = look.pieces
        .map(p => matched.items.find(i => !i.isOriginal && i.name === p.name)?.image)
        .filter((img): img is string => !!img);

      const allImgs = [originalImg, ...matchImgs].filter((img): img is string => !!img);
      if (!allImgs.length) return look;

      anyPatched = true;
      return { ...look, images: allImgs };
    });

    if (anyPatched) {
      localStorage.setItem("nomi_community_looks", JSON.stringify(patched));
    }

    setLooks(patched);
    setSavedIds(new Set(JSON.parse(localStorage.getItem("nomi_community_saved") ?? "[]")));
  }, []);

  const feed = useMemo(() => {
    let list = [...looks];
    if (activeTab === "All") {
      list.sort((a, b) => b.sharedAt - a.sharedAt);
    } else {
      const match = OCCASION_FILTER[activeTab] ?? [];
      list = list.filter(l => l.tags.some(t => match.includes(normalizeTag(t))));
    }
    if (query.trim()) {
      const q    = query.toLowerCase();
      const qTag = q.startsWith("#") ? q : `#${q}`;
      list = list.filter(l =>
        l.caption.toLowerCase().includes(q) ||
        l.tags.some(t => t.toLowerCase().includes(q) || t.toLowerCase() === qTag) ||
        l.pieces.some(p => p.name.toLowerCase().includes(q))
      );
    }
    return list;
  }, [looks, activeTab, query]);

  const trendingTags = useMemo(() => {
    const counts: Record<string, number> = {};
    looks.forEach(l => {
      l.tags.forEach(t => {
        const n = normalizeTag(t);
        if (n) counts[n] = (counts[n] ?? 0) + 1;
      });
    });
    return Object.entries(counts)
      .sort((a, b) => b[1] - a[1])
      .slice(0, 10)
      .map(([tag]) => tag);
  }, [looks]);

  const vibeTiles = useMemo(() => {
    return OCCASION_GROUPS.flatMap(g => [...g.options])
      .map(opt => {
        const slug = normalizeTag(opt);
        const cover = looks
          .filter(l => l.tags.some(t => normalizeTag(t) === slug) && !!l.image)
          .sort((a, b) => b.sharedAt - a.sharedAt)[0];
        return cover ? { label: opt, slug, image: cover.image! } : null;
      })
      .filter((t): t is NonNullable<typeof t> => t !== null);
  }, [looks]);

  function handleVibeTap(slug: string) {
    const tab = slugToFilterTab(slug);
    if (!tab) return;
    setActiveTab(tab);
    setTimeout(() => feedRef.current?.scrollIntoView({ behavior: "smooth" }), 50);
  }


  function handleSave(look: CommunityLook, e: React.MouseEvent) {
    e.stopPropagation();
    const isSaved = savedIds.has(look.id);
    setSavedIds(prev => {
      const next = new Set(prev);
      if (isSaved) {
        next.delete(look.id);
      } else {
        next.add(look.id);
        // Increment savedCount on the stored look
        const all: CommunityLook[] = JSON.parse(localStorage.getItem("nomi_community_looks") ?? "[]");
        const updated = all.map(l => l.id === look.id ? { ...l, savedCount: l.savedCount + 1 } : l);
        localStorage.setItem("nomi_community_looks", JSON.stringify(updated));
        setLooks(updated);
        // Update taste profile silently
        updateTasteProfile(look);
      }
      localStorage.setItem("nomi_community_saved", JSON.stringify([...next]));
      return next;
    });
  }

  function updateTasteProfile(look: CommunityLook) {
    try {
      const profile = JSON.parse(localStorage.getItem("nomi_taste_profile") ?? "{}");
      if (!profile.aesthetics) profile.aesthetics = {};
      look.tags.forEach(tag => {
        profile.aesthetics[tag] = (profile.aesthetics[tag] ?? 0) + 1;
      });
      if (!profile.savedCategories) profile.savedCategories = {};
      look.pieces.forEach(piece => {
        if (piece.category)
          profile.savedCategories[piece.category] = (profile.savedCategories[piece.category] ?? 0) + 1;
      });
      localStorage.setItem("nomi_taste_profile", JSON.stringify(profile));
    } catch { /* ignore */ }
  }

  return (
    <>
      <div style={{ minHeight: "100vh", background: "#fff", display: "flex", flexDirection: "column", alignItems: "center" }}>
        <div style={{ width: "100%", maxWidth: "420px" }}>

          {/* ── Sticky header ── */}
          <div style={{ position: "sticky", top: 0, background: "#fff", zIndex: 10 }}>
            <div style={{ padding: "20px 16px 10px" }}>
              <h1 style={{ fontSize: "22px", fontWeight: 600, letterSpacing: "-0.5px", marginBottom: "12px" }}>Explore</h1>
              {/* Search */}
              <div style={{ position: "relative" }}>
                <div style={{ position: "absolute", left: "13px", top: "50%", transform: "translateY(-50%)", color: "#aaa", pointerEvents: "none", lineHeight: 0 }}>
                  <SearchIcon />
                </div>
                <input
                  type="text" placeholder="Search looks, occasions, items..."
                  value={query} onChange={e => setQuery(e.target.value)}
                  style={{ width: "100%", padding: "11px 36px 11px 38px", borderRadius: "14px", border: `1.5px solid ${query ? "#000" : "#e8e8e8"}`, fontSize: "14px", color: "#000", background: "#f7f6f3", outline: "none", transition: "border-color 0.15s", boxSizing: "border-box" } as React.CSSProperties}
                />
                {query && (
                  <button onClick={() => setQuery("")} style={{ position: "absolute", right: "12px", top: "50%", transform: "translateY(-50%)", background: "none", border: "none", cursor: "pointer", color: "#aaa", lineHeight: 0, padding: 0 }}>
                    <XIcon />
                  </button>
                )}
              </div>
            </div>
            <div style={{ display: "flex", gap: "8px", overflowX: "auto", paddingLeft: "16px", paddingRight: "16px", paddingBottom: "12px", scrollbarWidth: "none" }}>
              {FILTER_TABS.map(tab => {
                const on = activeTab === tab;
                return (
                  <button key={tab} onClick={() => setActiveTab(tab)} style={{
                    flexShrink: 0, padding: "7px 16px", borderRadius: "99px", border: "none",
                    background: on ? "#c9a96e" : "#f0ede8",
                    color: on ? "#fff" : "#6b6b6b",
                    fontSize: "13px", fontWeight: on ? 600 : 400,
                    cursor: "pointer", whiteSpace: "nowrap",
                    transition: "background 0.15s, color 0.15s",
                  }}>
                    {tabLabel(tab)}
                  </button>
                );
              })}
            </div>
            {trendingTags.length > 0 && (
              <div>
                <p style={{ padding: "8px 16px 4px", fontSize: "10px", fontWeight: 600, color: "#bbb", letterSpacing: "0.6px", textTransform: "uppercase" }}>Trending tags</p>
                <div style={{ display: "flex", gap: "6px", overflowX: "auto", paddingLeft: "16px", paddingRight: "16px", paddingBottom: "10px", scrollbarWidth: "none" } as React.CSSProperties}>
                  {trendingTags.map(tag => {
                    const active = query.toLowerCase() === tag.toLowerCase();
                    return (
                      <button key={tag} onClick={() => setQuery(active ? "" : tag)} style={{
                        flexShrink: 0, padding: "5px 12px", borderRadius: "99px",
                        border: `1px solid ${active ? "#c9a96e" : "#e0dbd4"}`,
                        background: "none", cursor: "pointer", whiteSpace: "nowrap",
                        color: active ? "#c9a96e" : "#999",
                        fontSize: "12px", fontWeight: active ? 500 : 400,
                        transition: "color 0.15s, border-color 0.15s",
                        fontFamily: "inherit",
                      }}>
                        {tag}
                      </button>
                    );
                  })}
                </div>
              </div>
            )}
            <div style={{ height: "1px", background: "#ebebeb" }} />
          </div>

          {/* ── What's Hot ── */}
          {activeTab === "All" && trends.length > 0 && (
            <div style={{ padding: "16px 0 4px" }}>
              <div style={{ padding: "0 16px 10px" }}>
                <p style={{ fontSize: "13px", fontWeight: 600, color: "#000", letterSpacing: "-0.2px" }}>What&apos;s Hot</p>
                <p style={{ fontSize: "11px", color: "#bbb", marginTop: "2px" }}>
                  Weekly fashion digest{trendsGeneratedAt ? ` · Updated ${formatTrendDate(trendsGeneratedAt)}` : ""}
                </p>
              </div>
              <div style={{ display: "flex", gap: "10px", overflowX: "auto", paddingLeft: "16px", paddingRight: "16px", paddingBottom: "4px", scrollbarWidth: "none" } as React.CSSProperties}>
                {trends.map((trend, i) => (
                  <div key={i} style={{
                    flexShrink: 0, width: "calc(82% - 8px)",
                    borderRadius: "14px",
                    border: "1px solid #ede8e0", borderLeft: "3px solid #c9a96e",
                    background: "#faf7f3",
                    padding: "14px 14px 14px 13px",
                  }}>
                    <p style={{ fontSize: "13px", fontWeight: 600, color: "#000", letterSpacing: "-0.2px", lineHeight: 1.3 }}>{trend.title}</p>
                    <p style={{ fontSize: "12px", color: "#555", lineHeight: 1.65, marginTop: "7px" }}>{trend.summary}</p>
                    <p style={{ fontSize: "11px", color: "#bbb", marginTop: "9px" }}>
                      via{" "}
                      {trend.sources.map((src, j) => (
                        <span key={j}>
                          {j > 0 && ", "}
                          {src.url
                            ? <a href={src.url} target="_blank" rel="noopener noreferrer" style={{ color: "#bbb", textDecoration: "underline", textUnderlineOffset: "2px" }}>{src.name}</a>
                            : src.name
                          }
                        </span>
                      ))}
                    </p>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* ── Browse by vibe ── */}
          {activeTab === "All" && vibeTiles.length > 0 && (
            <div style={{ padding: "16px 16px 0" }}>
              <p style={{ fontSize: "13px", fontWeight: 600, color: "#000", letterSpacing: "-0.2px", marginBottom: "10px" }}>Browse by vibe</p>
              <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr", gap: "8px", marginBottom: "8px" }}>
                {vibeTiles.map(tile => (
                  <VibeTile key={tile.slug} label={tile.label} image={tile.image} onTap={() => handleVibeTap(tile.slug)} />
                ))}
              </div>
            </div>
          )}

          {/* ── Pinterest import ── */}
          {activeTab === "All" && <PinterestSection />}

          {/* ── Feed ── */}
          {looks.length === 0 ? (
            <EmptyState onShare={() => router.push("/saved")} />
          ) : feed.length === 0 ? (
            <div style={{ display: "flex", flexDirection: "column", alignItems: "center", padding: "60px 32px", textAlign: "center", gap: "10px" }}>
              <p style={{ fontSize: "15px", color: "#555" }}>No looks found for that search.</p>
              <button onClick={() => setQuery("")} style={{ background: "none", border: "none", cursor: "pointer", color: "#c9a96e", fontSize: "14px", fontWeight: 500, padding: 0 }}>Clear search</button>
            </div>
          ) : (
            <div ref={feedRef} style={{ display: "flex", flexDirection: "column", gap: "10px", padding: "10px 16px 80px" }}>
              {feed.map(look => (
                <CommunityCard
                  key={look.id} look={look}
                  isSaved={savedIds.has(look.id)}
                  onTap={() => setSelected(look)}
                  onSave={e => handleSave(look, e)}
                />
              ))}
            </div>
          )}
        </div>
      </div>

      <NomiNav />

      {selected && (
        <CommunityDetail
          look={looks.find(l => l.id === selected.id) ?? selected}
          isSaved={savedIds.has(selected.id)}
          onSave={e => handleSave(selected, e)}
          onClose={() => setSelected(null)}
          onTagTap={tag => { setQuery(tag); setSelected(null); }}
        />
      )}
    </>
  );
}

// ─── Community Card ───────────────────────────────────────────────────────────

function CommunityCard({ look, isSaved, onTap, onSave }: {
  look: CommunityLook; isSaved: boolean;
  onTap: () => void; onSave: (e: React.MouseEvent) => void;
}) {
  const allImages = look.images?.length ? look.images : (look.image ? [look.image] : []);
  const slots     = allImages.slice(0, 4);
  const hashTags  = look.tags.filter(t => t.startsWith("#"));
  const stores    = [...new Set(look.pieces.map(p => p.store).filter(Boolean))] as string[];
  const storeLabel = stores.length === 1 ? stores[0] : null;

  return (
    <div onClick={onTap} style={{ border: "1px solid #ebebeb", borderRadius: "16px", background: "#f7f6f3", cursor: "pointer", overflow: "hidden" }}>
      {/* Thumbnail strip — only render slots we have actual images for */}
      <div style={{ display: "flex", gap: "4px", padding: "12px 12px 8px" }}>
        {slots.map((img, i) => (
          <div key={i} style={{ flex: 1, aspectRatio: "1", borderRadius: "8px", overflow: "hidden", background: PLACEHOLDER_COLORS[i % 4] }}>
            <ItemThumbnail src={img} imageTier={look.imageTiers?.[i]} />
          </div>
        ))}
      </div>

      {/* Meta row */}
      <div style={{ padding: "0 12px 12px", display: "flex", alignItems: "flex-start", justifyContent: "space-between", gap: "8px" }}>
        <div style={{ flex: 1, minWidth: 0 }}>
          {look.caption && (
            <p style={{ fontSize: "12px", fontWeight: 600, color: "#000", letterSpacing: "-0.1px", lineHeight: 1.3, marginBottom: "3px", overflow: "hidden", whiteSpace: "nowrap", textOverflow: "ellipsis" }}>
              {look.caption}
            </p>
          )}
          <p style={{ fontSize: "11px", color: "#aaa", overflow: "hidden", whiteSpace: "nowrap", textOverflow: "ellipsis" }}>
            {look.posterName}
            {look.pieces.length > 0 ? ` · ${look.pieces.length} ${look.pieces.length === 1 ? "piece" : "pieces"}` : ""}
            {storeLabel ? ` · ${storeLabel}` : ""}
          </p>
          {hashTags.length > 0 && (
            <p style={{ fontSize: "10px", color: "#ccc", marginTop: "2px" }}>{hashTags.slice(0, 2).join(" ")}</p>
          )}
        </div>
        <button
          onClick={onSave}
          style={{ background: "none", border: "none", cursor: "pointer", padding: "2px 0", flexShrink: 0, lineHeight: 0 }}
        >
          {isSaved ? <BookmarkFillSm /> : <BookmarkOutlineSm />}
        </button>
      </div>
    </div>
  );
}

